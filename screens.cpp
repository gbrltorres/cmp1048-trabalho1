void clear_screen(){
    for(int i=0; i<100; i++)
        printf("\n");
}

void print_charge_bar(char* arr, int n){
    printf("[");
    for(int i=0; i<n; i++){
        printf("%c", arr[i]);
    }
    printf("]");
    printf("\n");
}

void charge_bar(int n){
    clear_screen();
    int m = 15;
    int r = n*m;
    char charge_bar[r];
    for(int i=0; i<r; i++){
        charge_bar[i] = '=';
    }
    for(int i=0; i<r; i++){
        print_charge_bar(charge_bar, r);
        fflush(stdout);
        usleep(1000000/r);
        clear_screen();
        charge_bar[i] = '#';
    }
}
